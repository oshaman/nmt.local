                        обнаружено нарушение. Процедура возврата простая: районное управление выставляет счет на сумму
                        <a href="/">субсидии</a> которая не на законных основаниях была назначена, и заявитель обязан на
                        лицевой счет, который будет указан в бланке, эту сумму перечислить. По истечении года он может
                        снова подать заявление на субсидию.</p>

